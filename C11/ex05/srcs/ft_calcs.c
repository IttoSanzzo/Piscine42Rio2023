#include <ft.h>

int	ft_plus(int i1, int i2)
{
	return (i1 + i2);
}

int	ft_less(int i1, int i2)
{
	return (i1 - i2);
}

int	ft_time(int i1, int i2)
{
	return (i1 * i2);
}

int	ft_divi(int i1, int i2)
{
	return (i1 / i2);
}

int	ft_modu(int i1, int i2)
{
	return (i1 % i2);
}
